using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Отбор_в_разведку
{
    internal class Program
    {
        
            
            static void Main()
            {
                //////////////////////////////////////////////////////////   ОТБОР В РАЗВЕДКУ   ///////////////////////////////////////////////////////
                


                // Чтение входного значения N (количество солдат)
                int N = int.Parse(Console.ReadLine());

                // Вычисляем количество групп разведки
                int result = colvo_GRUPP(N);

                // Выводим результат
                Console.WriteLine(result);
                
            }

            static int colvo_GRUPP(int soldats) // рекурсивная функция которая занимается количеством подсчёта групп которые можно сформировать
            {
                // Если солдат меньше 3, группу создать нельзя
                if (soldats < 3)
                    return 0;

                // Если ровно 3 солдата, можно создать одну группу
                if (soldats == 3)
                    return 1;

                // Разбиваем шеренгу на четные и нечетные позиции
                int chet = soldats / 2;         // Количество солдат на четных позициях (целочисленное деление)
                int nechet = soldats - chet;       // Количество солдат на нечетных позициях

                // Рекурсивно считаем количество групп для обеих частей
                return colvo_GRUPP(chet) + colvo_GRUPP(nechet);
            }


                ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    }
}
